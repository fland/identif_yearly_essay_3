public class DirectFlowData {

    private double c;

    private double b;

    private double d;

    private double f;

    private double g;

    public DirectFlowData(double c, double b, double d, double f, double g) {
        this.c = c;
        this.b = b;
        this.d = d;
        this.f = f;
        this.g = g;
    }

    public double getC() {
        return c;
    }

    public double getB() {
        return b;
    }

    public double getD() {
        return d;
    }

    public double getF() {
        return f;
    }

    public double getG() {
        return g;
    }
}
